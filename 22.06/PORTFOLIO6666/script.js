console.log('=== ЗАДАЧА 6: Космический передатчик ===');

for (let i = 1; i <= 50; i++) {
    if (i % 4 === 0 && i % 7 === 0) {
        console.log(`Импульс ${i}: BeepBoop`);
    } else if (i % 4 === 0) {
        console.log(`Импульс ${i}: Beep`);
    } else if (i % 7 === 0) {
        console.log(`Импульс ${i}: Boop`);
    } else {
        console.log(`Импульс ${i}: ${i}`);
    }
}

console.log('');

// ==============================================
// ЗАДАЧА 7: «Гипотеза Сиракуз» (Сложное изменение переменной)
// ==============================================

console.log('=== ЗАДАЧА 7: Гипотеза Сиракуз ===');

let n = 27;
console.log(`Начальное число: ${n}`);

for (let step = 1; step <= 15; step++) {
    if (n % 2 === 0) {
        n = n / 2;
    } else {
        n = n * 3 + 1;
    }
    console.log(`Шаг ${step}: ${n}`);
}

console.log('');

