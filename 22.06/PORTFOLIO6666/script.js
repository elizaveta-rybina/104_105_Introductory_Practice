// ==============================================

console.log('=== ЗАДАЧА 1: Умный пропускной пункт ===');

const age = 16;        // Возраст посетителя
const height = 145;    // Рост в сантиметрах

console.log(`Возраст: ${age} лет, Рост: ${height} см`);

if (age < 8) {
    console.log('❌ Вход запрещен, слишком мал');
} else if (age >= 8 && age <= 18 && height < 140) {
    console.log('👨‍👦 Вход только с родителями');
} else if (age > 18 || (age >= 8 && height >= 140)) {
    console.log('✅ Добро пожаловать на аттракцион!');
} else {
    console.log('❌ Доступ запрещен');
}

console.log('');

// ==============================================
// ЗАДАЧА 2: «Анализатор последовательности» (Циклы и Условия)
// ==============================================

console.log('=== ЗАДАЧА 2: Анализатор последовательности ===');

let sum = 0;

for (let i = 1; i <= 200; i++) {
    if (i % 3 === 0 && i % 5 !== 0) {
        sum += i;
    }
}

console.log(`Сумма чисел от 1 до 200, которые делятся на 3, но не делятся на 5: ${sum}`);

console.log('');

// ==============================================
// ЗАДАЧА 3: «Шифратор» (Математика в цикле)
// ==============================================

console.log('=== ЗАДАЧА 3: Шифратор ===');

for (let i = 15; i >= 1; i--) {
    if (i % 2 === 0) {
        console.log(`Четное: ${i}`);
    } else {
        console.log(`Нечетное * 2 = ${i * 2}`);
    }
}

console.log('');

// ==============================================
// ЗАДАЧА 4: «Финансовый симулятор» (Накопление в цикле)
// ==============================================

console.log('=== ЗАДАЧА 4: Финансовый симулятор ===');

let capital = 10000;
const years = 10;
const rate = 0.08; // 8% годовых

console.log(`Стартовый капитал: ${capital} руб.`);

for (let year = 1; year <= years; year++) {
    capital = capital + capital * rate;
    console.log(`Год ${year}: ${Math.round(capital)} руб.`);
}

console.log(`Итоговая сумма через ${years} лет: ${Math.round(capital)} руб.`);

console.log('');

